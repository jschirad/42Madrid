#include "libft.h"

int		ft_isalnum(int c)
{
	int x;

	x = 1;
	if (!((c >= 48 && c <= 57) || (c >= 97 && c <= 122)
	|| (c >= 65 && c <= 90)))
		x = 0;
	return (x);
}
